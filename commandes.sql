CREATE VIEW tableaudebord AS 
SELECT C.id, C.titre, ROUND(AVG(N.note), 2) AS Moyenne 
FROM notes N 
INNER JOIN certifications C 
ON N.idcertification = C.id 
GROUP BY C.id, C.titre 
HAVING Moyenne < 75;


